  var day = now.day;
  print("Day: $day");
  
  // Log the current hour
  var hour = now.hour;
  print("Day: $hour");
  
  // Log the current minute
  var minute = now.minute;
  print("minute: $minute");
 
  // Log the current second
  var second = now.second;
  print("Day: $second");
}
